    class Solution {
        //åé©±èŠ‚ç‚¹
        ListNode successor = null;

        //é€’å½’åè½¬é“¾è¡¨å‰kä¸ªèŠ‚ç‚¹
        public ListNode reverseList(ListNode head, Integer k) {
            if (k == 1) {
                successor = head.next;
                return head;
            }
            ListNode last = reverseList(head.next, k - 1);
            head.next.next = head;
            head.next = successor;
            return last;
        }

        //è¿­ä»£åè½¬é“¾è¡¨å‰kä¸ªèŠ‚ç‚¹
        public ListNode reverseList2(ListNode head, Integer k) {
            ListNode pre = null;
            ListNode cur = head;
            ListNode next;
            while (k-- > 0) {
                next = cur.next;
                cur.next = pre;
                pre = cur;
                cur = next;
            }
            head.next = cur;
            return pre;
        }

        //é€’å½’æ³•
        public ListNode reverseList(ListNode head) {
            if (head == null || head.next == null) {
                return head;
            }
            ListNode last = reverseList(head.next);
            head.next.next = head;
            head.next = null;
            return last;
        }

        //è¿­ä»£æ³•
        public ListNode reverseList3(ListNode head) {
            ListNode pre = null;
            ListNode cur = head;
            ListNode next;
            while (cur != null) {
                next = cur.next;
                cur.next = pre;
                pre = cur;
                cur = next;
            }
            return pre;
        }
    }
