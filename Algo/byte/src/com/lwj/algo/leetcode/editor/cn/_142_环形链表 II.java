    /**
     * Definition for singly-linked list.
     * class ListNode {
     * int val;
     * ListNode next;
     * ListNode(int x) {
     * val = x;
     * next = null;
     * }
     * }
     */
    public class Solution {
        public ListNode detectCycle(ListNode head) {
            ListNode q = head;
            ListNode s = head;
            while (q != null && q.next != null) {
                q = q.next.next;
                s = s.next;
                if (q == s) {
                    //ä¸¤é“¾è¡¨ç›¸äº¤
                    q = head;
                    while (q != s) {
                        q = q.next;
                        s = s.next;
                    }
                    return q;
                }
            }
            return null;
        }

        public ListNode detectCycle2(ListNode head) {
            if (head == null) {
                return null;
            }
            ListNode fast = head, slow = head;
            while (fast != null && fast.next != null) {
                fast = fast.next.next;
                slow = slow.next;
                if (fast == slow) {
                    break;
                }
            }
            if (fast == null || fast.next == null) {
                return null;
            }
            slow = head;
            while (fast != slow) {
                fast = fast.next;
                slow = slow.next;
            }
            return fast;
        }
    }
