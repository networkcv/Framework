
"""
# 文件
通过Python内置的`open`函数，我们可以指定文件名、操作模式、编码信息等来获得操作文件的对象，接下来就可以对文件进行读写操作了。这里所说的操作模式是指要打开什么样的文件（字符文件还是二进制文件）以及做什么样的操作（读、写还是追加），具体的如下表所示。

| 操作模式 | 具体含义                         |
| -------- | -------------------------------- |
| `'r'`    | 读取 （默认）                      |
| `'w'`    | 写入（会先截断之前的内容）           |
| `'x'`    | 写入，如果文件已经存在会产生异常      |
| `'a'`    | 追加，将内容写入到已有文件的末尾      |
| `'b'`    | 二进制模式                        |
| `'t'`    | 文本模式（默认）                   |
| `'+'`    | 更新（既可以读又可以写）            |

"""

